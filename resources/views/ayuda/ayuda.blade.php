realiza a trav&eacute;s de la opci&oacute;n &ldquo;<b>Alta de Juicio</b>&rdquo;
en el <b>Men&uacute; Principal. </b><span style="font-weight: normal">Los
datos obligatorios son: Causa, Expediente Judicial, Descripci&oacute;n
y Fecha de inicio de juicio. Los datos que se encuentran en un men&uacute;
desplegable toman la opci&oacute;n disponible como valor por defecto.
Si el dato de Vencimiento se deja en blanco se toma como que el
juicio ingresa sin fecha de vencimiento.</span></p>
<h2 class="western"><a name="__RefHeading___Toc427_1156424826"></a>Modificaci&oacute;n
</h2>
<p class="western"><a name="__RefHeading___Toc495_1156424826"></a>	Se
pueden modificar todos los datos del juicio menos el n&uacute;mero de
expediente judicial. Todas las modificaciones que se realicen se
guardar&aacute;n en el historial del juicio.</p>
<h2 class="western"><a name="__RefHeading___Toc429_1156424826"></a>Eventos
de Juicio</h2>
<p class="western"><a name="__RefHeading___Toc497_1156424826"></a>	Los
eventos del juicio reflejan todas las acciones que se realizaron
durante el juicio. Es la forma por defecto de ingresar informaci&oacute;n
al juicio.</p>
<h2 class="western"><a name="__RefHeading___Toc431_1156424826"></a>Cierre</h2>
<p class="western"><a name="__RefHeading___Toc499_1156424826"></a>	Una
vez finalizado el juicio se podr&aacute; cerrarlo para que no se
pueda agregar m&aacute;s informaci&oacute;n pero estar&aacute;
disponible para consulta desde el men&uacute; de Juicios Cerrados.</p>
<h2 class="western"><a name="__RefHeading___Toc433_1156424826"></a>Reapertura</h2>
<p class="western">	La reapertura del juicio puede hacerla el abogado
encargado o jefe del &aacute;rea. 
</p>
<h2 class="western"><a name="__RefHeading___Toc501_1156424826"></a>Eliminaci&oacute;n
definitiva</h2>
<p class="western"><a name="__RefHeading___Toc503_1156424826"></a>	En
caso de desear eliminar un juicio en forma definitiva deber&aacute;n
contactarse con la Gerencia de Sistemas.</p>
