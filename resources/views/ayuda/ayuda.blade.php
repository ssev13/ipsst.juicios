<p class="western" style="line-height: 120%; background: transparent">
		<u>Etiquetas</u>: Las distintas etiquetas que se utilizan para
		marcar los juicios a fin de facilitar su b&uacute;squeda y para
		realizar los informes estad&iacute;sticos.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Estados</u>: Los distintos estados en los que se puede encontrar
		un juicio en un momento determinado.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Tipos de Evento</u>: El listado de eventos que pueden ocurrir
		dentro de un tr&aacute;mite judicial.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Objetos</u>: Los distintos objetos que puede tener un juicio.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Juzgados</u>: El listado de juzgados y cortes en los que puede
		estar asignado un juicio.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Sentencias</u>: Los distintos estados que puede tener una
		sentencia de un juicio.</p>
	</ol>
	<li/>
<p class="western" style="line-height: 120%; background: transparent">
	<b>Men&uacute; de Usuario</b>: Se muestran distintas opciones seg&uacute;n
	el tipo de usuario y las herramientas para actualizar la informaci&oacute;n
	de la cuenta y de cierre de sesi&oacute;n.</p>
	<li/>
<p class="western" style="line-height: 120%; background: transparent">
	<b>Barra de B&uacute;squeda</b>: El texto que se ingrese en esta
	barra se utilizar&aacute; para buscar por <i><u><span style="font-weight: normal">Causa,
	Expediente Judicial y Descripci&oacute;n</span></u></i>, tanto de
	los juicios activos como inactivos.</p>
	<li/>
<p class="western" style="line-height: 120%; background: transparent">
	<b>Estad&iacute;stica Usuarios</b>: Muestra la cantidad de usuarios
	registrados en el sistema. Si se sigue el enlace marcado se puede
	acceder a un listado de todos los usuarios y la cantidad de juicios
	que tiene asignado cada uno. Se pueden realizar b&uacute;squedas y
	ordenar el listado seg&uacute;n cada una de sus columnas haciendo
	click en el t&iacute;tulo.</p>
	<li/>
<p class="western" style="line-height: 120%; background: transparent">
	<b>Estad&iacute;stica Juicios Asignados</b>: Muestra dos n&uacute;meros,
	el primero es la cantidad de juicios que tiene asignado el usuario y
	el segundo es el n&uacute;mero total de juicios activos. Si se sigue
	el enlace marcado se puede acceder a la lista de juicios que tiene a
	cargo el usuario conectado.</p>
	<li/>
<p class="western" style="line-height: 120%; background: transparent">
	<b>Estad&iacute;stica Vencimientos Semana</b>: Muestra dos n&uacute;meros,
	el primero es la cantidad de juicios con vencimiento hasta el lunes
	pr&oacute;ximo que tiene asignado el usuario y el segundo es el
	n&uacute;mero total de juicios con vencimientos en el mismo per&iacute;odo
	de tiempo. Si se sigue el enlace marcado se puede acceder a la lista
	total de juicios vencidos.</p>
	<li/>
<p class="western" style="line-height: 120%; background: transparent">
	<b>Listado de Juicios con Vencimiento</b>: Esta lista es de los
	juicios encargados al usuario conectado que tienen vencimiento hasta
	el pr&oacute;ximo lunes.</p>
