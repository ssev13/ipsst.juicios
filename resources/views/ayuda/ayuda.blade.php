<h1 class="western" style="page-break-before: always"><a name="__RefHeading___Toc409_1156424826"></a>
Reportes 
</h1>
<p class="western">	La opci&oacute;n de Reportes que se encuentra en
el Men&uacute; Principal permite filtrar los juicios a fin de contar
con un listado acotado que puede ser usado como b&uacute;squeda
avanzada o impreso para utilizar en alg&uacute;n informe.</p>
<p class="western">	Las opciones por las que permite filtrar son:</p>
<p class="western"><b>Fecha de inicio del juicio</b>. Se cuenta con
dos campos donde se puede colocar fecha desde y fecha hasta cuando se
desea filtrar.</p>
<p class="western"><b>Vencimiento del juicio</b>. Se cuenta con dos
campos donde se puede colocar fecha desde y fecha hasta cuando se
desea filtrar.</p>
<p class="western"><b>Abogado</b>: Se puede elegir el abogado
encargado del juicio.</p>
<p class="western"><b>Descripci&oacute;n</b>: Ac&aacute; se puede
escribir alguna palabra o frase que se buscar&aacute; en el campo
Descripci&oacute;n del juicio.</p>
<p class="western"><b>Juzgado</b>: Se puede filtrar por Juzgado donde
se encuentra actualmente el tr&aacute;mite.</p>
<p class="western"><b>Objeto</b>: Filtra por Objeto del Juicio</p>
<p class="western"><b>Estado</b>: Filtra por el Estado actual del
Juicio.</p>
<p class="western"><b>Sentencia</b>: Filtra por el estado actual de
la Sentencia del Juicio.</p>
<p class="western"><br/>
<br/>

</p>
<p class="western">	Una vez seleccionadas todas las opciones deseadas
se presiona el bot&oacute;n <b>Buscar</b> y se mostrar&aacute; el
listado de juicios que cumplen esas condiciones. En ese listado hay
dos botones.</p>
