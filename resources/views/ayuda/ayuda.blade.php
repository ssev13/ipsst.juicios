		profesional.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Listado de Juicios</u>: Muestra todos los juicios activos. Tiene
		los botones de Historial, Alta de Eventos y Edici&oacute;n de
		Juicios.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Reportes</u>:Muestra una lista de opciones por los que se puede
		filtrar el listado de juicios para una b&uacute;squeda particular.
		Tambi&eacute;n se puede imprimir el resultado.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Juicios Cerrados</u>:  Muestra todos los juicios inactivos.
		Tiene los botones de Historial, Reapertura de Juicio y Eliminaci&oacute;n
		definitiva.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Estad&iacute;sticas</u>: Muestra distintos gr&aacute;ficos que
