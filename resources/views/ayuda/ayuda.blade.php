		campo de b&uacute;squeda e informaci&oacute;n resumida sobre el
		estado de los juicios.</p>
		<li/>
<p class="western" style="line-height: 120%; background: transparent">
		<u>Alta de Juicios</u>: Es la opci&oacute;n para crear juicios
